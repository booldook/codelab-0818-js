/*
변수(상수), 데이터 타입
- 변수: 변하는 값 (var, let)
- 상수: 변하지 않는 값 (const)
- 데이터 타입: primitive type(원시타입), reference type(참조타입)
  - 원시타입: char(문자), string(문자열), number(숫자)
  - 참조타입: Array(배열), Object(객체), null(빈값), Set, Map, function ...
*/

var a; //  a 라는 변수를 선언함.
a = "ABC"; // a에 "ABC"문자열을 대입함.
var a = "ABC"; // 축약

